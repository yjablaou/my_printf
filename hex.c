#include "ft_printf.h"

int	hex(unsigned long n)
{
	const char	c[] = "0123456789abcdef";
	int			p;

	p = 0;
	if (n >= 16)
	{
		p += hex(n / 16);
		p += hex(n % 16);
	}
	else
		p += ft_putchar(c[n]);
	return(p);
}
