#include "ft_printf.h"

int	ft_putu(unsigned int n)
{
	char	c;
	int		p;

	p = 0;
	if (n >= 10)
	{
		p += ft_putnbr(n / 10);

	}
	c = n % 10 + '0';
	p += ft_putchar(c);
	return (p);
}
